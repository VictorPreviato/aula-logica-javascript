// vetores array

let notas = [7.5, 6.3, 4.8, 9.2];

console.log("Notas iniciais", notas);

// ordernar em ordem crescente

notas.sort((a, b) => a - b);

console.log("Notas ordenadas (crescente): ", notas);

// ordernar em ordem decrescente

notas.sort((a,b) => b - a);

console.log("Notas ordenadas (crescente): ", notas);

